#include <iostream>

// Se importa el espacio de nombres "std"
// Todos los elementos de la librería estándar C++ están declarados bajo
// el espacio de nombres std
using namespace std;


int main()
{

	std::cout << "¡Hola Mundo!" << std::endl;

	cout << "Al declarar 'using namespace std' ya no es necesario utilizar el prefijo std::";
	cout << "en las elementos de entrada o salida de datos." << endl;


	return 0;
}
